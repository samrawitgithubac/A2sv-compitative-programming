class Solution:
    def applyOperations(self, nums: List[int]) -> List[int]:
        for i in range(1,len(nums)):
            if nums[i-1]==nums[i]:
                nums[i-1]=2*nums[i-1]
                nums[i]=0
        l=0
        res=[]
        count=0
        for i in nums:
            if i!=0:
                res.append(i)
            else:
                count+=1
        for i in range(count):
            res.append(0)
        return res


       



    









































































































































































































        